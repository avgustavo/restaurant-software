    /**
     * Metodo que insere um pedido no repositorio e atualiza a lista de pedidos
     * @param evt
     */
    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        // TODO add your handling code here:
        if(listaAdicionados.getModel().getSize()!=0)
        {
        if (categoriaPedido.equals("mesa")) {
            Pedido p = new PedidoMesa(mesa);
            Fachada.getInstancia().inserirPedido(p);
            for (int i = 0; i < model1.getSize(); i++) {

                for (Item item : Fachada.getInstancia().listarItensArray()) {

                    if (model1.get(i).equals(item.getNome())) {
                        p.inserirItem(item);

                    }
                }
            }
        } else if (categoriaPedido.equals("balcao")) {

            Pedido k = new PedidoBalcao();
            Fachada.getInstancia().inserirPedido(k);
            for (int i = 0; i < model1.getSize(); i++) {

                for (Item item : Fachada.getInstancia().listarItensArray()) {

                    if (model1.get(i).equals(item.getNome())) {
                        k.inserirItem(item);

                    }
                }
            }
        } else if (categoriaPedido.equals("entrega")) {

            Pedido j = new PedidoEntrega();
            Fachada.getInstancia().inserirPedido(j);
            for (int i = 0; i < model1.getSize(); i++) {

                for (Item item : Fachada.getInstancia().listarItensArray()) {

                    if (model1.get(i).equals(item.getNome())) {
                        j.inserirItem(item);

                    }
                }
            }
            
            
    }
        TelaPedido.getInstancia().atualizaLista();
        dispose();
    }
    }//GEN-LAST:event_btnFinalizarActionPerformed

    /**
     * Metodo que adiciona itens ao pedido
     * @param evt
     */
    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
       
        if (abas.getSelectedComponent()==abaEntradas) {
                item = listaEntradas.getSelectedValue();
           
        }
        else if (abas.getSelectedComponent()==abaPratos) {
         
                item = listaPratos.getSelectedValue();
            
        }
        else if (abas.getSelectedComponent()==abaSobremesas) {
           
                item = listaSobremesas.getSelectedValue();
        }
        else if (abas.getSelectedComponent()==abaBebidas) {
           
                item = listaBebidas.getSelectedValue();
        }
       
        if(item!=null){ 
       model1.addElement(item);
       listaAdicionados.setModel(model1);
       for(Item i : Fachada.getInstancia().listarItensArray())
       {
           if(i.getNome().equals(item))
           {
               model2.addElement(i.getValor());
               valorTotal = valorTotal +i.getValor();
               break;
           }
       }
       listaValores.setModel(model2);
       txtValor.setText(String.valueOf(valorTotal));
       }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    /**
     * Metodo que remove itens do pedido
     * @param evt
     */
    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        item = listaAdicionados.getSelectedValue();
        for(Item i : Fachada.getInstancia().listarItensArray())
       {
           if(i.getNome().equals(item))
           {
               model2.removeElement(i.getValor());
               valorTotal = valorTotal - i.getValor();
               break;
           }
       }
       listaValores.setModel(model2);
        model1.removeElement(listaAdicionados.getSelectedValue());
        listaAdicionados.setModel(model1);
        txtValor.setText(String.valueOf(valorTotal));
       
    }//GEN-LAST:event_btnRemoverActionPerformed

